//3. Apenas os Pares
//Objetivo: Usar o incremento para pular números.
//
//Desafio: Faça um laço que vá de 0 até 20, mas que imprima apenas os números pares.
//
//Dica: Você pode mudar o incremento para i += 2 em vez de i++.

public class Exe19 {
    public static void main(String[] args) {
        for (int i = 0; i <= 20; i+=2){
            System.out.println(i);
        }
    }
}
