//Positivo ou Negativo: Receba um número e diga se ele é "Positivo", "Negativo" ou "Zero".

public class Exe5 {

    public static void main(String[] args){

        int numero = 0;

        if (numero > 0){
            System.out.println("Positivo");

        }
        else if (numero < 0) {
            System.out.println("Negativo");

        }
        else{
            System.out.println("Zero");
        }
    }
}
