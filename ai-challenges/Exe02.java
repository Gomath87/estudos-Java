//Par ou Ímpar: Receba um número inteiro e diga se ele é par ou ímpar (Dica: use o operador % que pega o resto da divisão).

public class Exe02 {
    public static void main(String[] args){
        int numero = 7;

        if (numero % 2 == 0){
            System.out.println("É par");

        }
        else{
            System.out.println("É ímpar");
        }

    }
}
