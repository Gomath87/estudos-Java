//Maioridade: Crie uma variável idade. Se for 18 ou mais, exiba "Maior de idade", caso contrário, "Menor de idade".

public class Exe01 {
    public static void main(String[] args){
        int idade = 17;

        if (idade >= 18){
            System.out.println("Maior de idade");
        }
        else{
            System.out.println("Menor de idade");
        }

    }
}

